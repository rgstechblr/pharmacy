package com.moaddi.ui.forms;

public class MachineSupForm {
	
	private Long[] supId;
	private Long[] connectedSupId;
	private Long machineId;
	public Long[] getSupId() {
		return supId;
	}
	public void setSupId(Long[] supId) {
		this.supId = supId;
	}
	public Long[] getConnectedSupId() {
		return connectedSupId;
	}
	public void setConnectedSupId(Long[] connectedSupId) {
		this.connectedSupId = connectedSupId;
	}
	public Long getMachineId() {
		return machineId;
	}
	public void setMachineId(Long machineId) {
		this.machineId = machineId;
	}
	
	
}
